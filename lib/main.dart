import 'package:flutter/material.dart';
import 'package:odia_bhagabata/global.dart';
import 'package:odia_bhagabata/pages/homePage.dart';
import 'package:shared_preferences/shared_preferences.dart';

void main() async {
  WidgetsFlutterBinding.ensureInitialized();

  // ✅ Load saved preferences, fallback to your defaults
  SharedPreferences prefs = await SharedPreferences.getInstance();
  showCover = prefs.getBool('showCover') ?? true;     // default: true
  isDarkMode = prefs.getBool('isDarkMode') ?? true;   // default: true

  runApp(MyApp());
}

class MyApp extends StatefulWidget {
  @override
  _MyAppState createState() => _MyAppState();
}

class _MyAppState extends State<MyApp> {
  void _toggleTheme() async {
    setState(() {
      isDarkMode = !isDarkMode;
    });

    // ✅ Save theme toggle state
    SharedPreferences prefs = await SharedPreferences.getInstance();
    prefs.setBool('isDarkMode', isDarkMode);
  }

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      theme: isDarkMode ? ThemeData.dark() : ThemeData.light(),
      home: HomePage(toggleTheme: _toggleTheme),
    );
  }
}


